"""
Распознавание названий-«пустышек», где вместо предмета закупки стоит только
ТИП ПРОЦЕДУРЫ (способ определения поставщика):
    «Запрос котировок в электронной форме», «Электронный аукцион»,
    «Закупка у единственного поставщика» и т.п.

Единый источник правды для парсера RSS, AI-генератора названий и форматтера
карточки — чтобы поведение совпадало во всех трёх местах.
"""

import re

# Полное совпадение: строка состоит ТОЛЬКО из типа процедуры,
# опционально «в электронной форме» и опционального номера.
PROCEDURE_ONLY_RE = re.compile(
    r'^\W*'
    r'(?:'
    r'закупк\w*\s+у\s+единственн\w+\s+поставщик\w*(?:\s*\([^)]*\))?'
    r'|конкурс\w*\s+с\s+ограниченн\w+\s+участи\w+'
    r'|двухэтапн\w+\s+конкурс\w*'
    r'|закрыт\w+(?:\s+электронн\w+)?\s+(?:аукцион\w*|конкурс\w*)'
    r'|электронн\w+\s+аукцион\w*'
    r'|открыт\w+\s+конкурс\w*'
    r'|предварительн\w+\s+отбор\w*'
    r'|запрос\w*\s+предложен\w+'
    r'|запрос\w*\s+котировок(?:\s+цен)?'
    r'|аукцион\w*'
    r'|конкурс\w*'
    r')'
    r'(?:\s+в\s+электронн\w+\s+форме)?'   # необязательный «в электронной форме»
    r'\s*(?:[№#]\s*[\w\d./-]+)?'           # необязательный номер процедуры
    r'\W*$',
    re.I,
)


def is_procedure_type_only(title: str) -> bool:
    """True, если строка — только тип процедуры (способ определения
    поставщика) без указания предмета закупки."""
    if not title:
        return False
    return bool(PROCEDURE_ONLY_RE.match(title.strip()))
