@router.callback_query(F.data == "admin_list_users")
async def list_users(callback: CallbackQuery):
    """–ü–æ–∫–∞–∑—ã–≤–∞–µ—Ç —Å–ø–∏—Å–æ–∫ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π —Å –¥–æ—Å—Ç—É–ø–æ–º."""
    if not is_admin(callback.from_user.id):
        await callback.answer("‚ùå –î–æ—Å—Ç—É–ø –∑–∞–ø—Ä–µ—â–µ–Ω", show_alert=True)
        return

    await callback.answer()

    users = access_manager.get_all_users()

    if not users:
        await callback.message.answer(
            "üìã <b>–°–ø–∏—Å–æ–∫ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π –ø—É—Å—Ç</b>\n\n"
            "–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–∏ —Å –¥–æ—Å—Ç—É–ø–æ–º –Ω–µ –Ω–∞–π–¥–µ–Ω—ã.",
            parse_mode="HTML"
        )
        return

    # –§–æ—Ä–º–∏—Ä—É–µ–º —Å–ø–∏—Å–æ–∫ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π
    text = "üë• <b>–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–∏ —Å –¥–æ—Å—Ç—É–ø–æ–º:</b>\n\n"

    for i, user in enumerate(users, 1):
        user_id = user['user_id']
        username = f"@{user['username']}" if user['username'] else "–Ω–µ—Ç username"
        first_name = user['first_name'] or ""
        last_name = user['last_name'] or ""
        full_name = f"{first_name} {last_name}".strip() or "–ò–º—è –Ω–µ —É–∫–∞–∑–∞–Ω–æ"
        added_at = user['added_at'][:10]  # –¢–æ–ª—å–∫–æ –¥–∞—Ç–∞

        text += (
            f"{i}. <b>{full_name}</b> ({username})\n"
            f"   ID: <code>{user_id}</code>\n"
            f"   –î–æ–±–∞–≤–ª–µ–Ω: {added_at}\n\n"
        )

    # –†–∞–∑–±–∏–≤–∞–µ–º –Ω–∞ –Ω–µ—Å–∫–æ–ª—å–∫–æ —Å–æ–æ–±—â–µ–Ω–∏–π, –µ—Å–ª–∏ —Å–ª–∏—à–∫–æ–º –¥–ª–∏–Ω–Ω–æ–µ
    if len(text) > 4000:
        chunks = []
        current_chunk = "üë• <b>–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–∏ —Å –¥–æ—Å—Ç—É–ø–æ–º:</b>\n\n"

        for i, user in enumerate(users, 1):
            user_text = (
                f"{i}. <b>{user['first_name'] or ''} {user['last_name'] or ''}</b> "
                f"(@{user['username'] or '–Ω–µ—Ç'})\n"
                f"   ID: <code>{user['user_id']}</code>\n"
                f"   –î–æ–±–∞–≤–ª–µ–Ω: {user['added_at'][:10]}\n\n"
            )

            if len(current_chunk) + len(user_text) > 4000:
                chunks.append(current_chunk)
                current_chunk = user_text
            else:
                current_chunk += user_text

        chunks.append(current_chunk)

        for chunk in chunks:
            await callback.message.answer(chunk, parse_mode="HTML")
    else:
        await callback.message.answer(text, parse_mode="HTML")


